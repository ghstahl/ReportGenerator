namespace Palmmedia.ReportGenerator
{
    /// <summary>
    /// Enumeration for the logging verbosity.
    /// </summary>
    internal enum VerbosityLevel
    {
        /// <summary>
        /// All messages are logged.
        /// </summary>
        Verbose,

        /// <summary>
        /// Only important messages are logged.
        /// </summary>
        Info,

        /// <summary>
        /// Only errors are logged.
        /// </summary>
        Error
    }
}
